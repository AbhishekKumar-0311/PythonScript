#in python to findout datatype of any variable , object use :type(variable name,object name)
inttype=type(343)
print('343 datatype:',inttype) #answer:343 datatype: <class 'int'>

floattype=type(5.34)
print('5.34 datatype:',floattype) #answer: 5.34 datatype: <class 'float'>

#in python single charcter also has datatype string
singlecharacter=type('p')
print('p datatype:',singlecharacter) #answer :p datatype: <class 'str'>

multiplecharacter=type('python')
print('python datatype:',multiplecharacter)#answer:python datatype: <class 'str'>



